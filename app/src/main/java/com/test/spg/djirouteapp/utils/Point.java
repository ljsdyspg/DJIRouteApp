package com.test.spg.djirouteapp.utils;

public class Point {
    public String Name;
    public double X;
    public double Y;
    public double Z;

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public double getX() {
        return X;
    }

    public void setX(double x) {
        X = x;
    }

    public double getY() {
        return Y;
    }

    public void setY(double y) {
        Y = y;
    }

    public double getZ() {
        return Z;
    }

    public void setZ(double z) {
        Z = z;
    }
}
